package rf.foundation.numbering;

public class NumberingSequence {
	private Long from = 0L;
	private Long to = 0L;

	public Long getFrom() {
		return from;
	}

	public void setFrom(Long from) {
		this.from = from;
	}

	public Long getTo() {
		return to;
	}

	public void setTo(Long to) {
		this.to = to;
	}

}
